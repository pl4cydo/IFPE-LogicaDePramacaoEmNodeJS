//Elevar um número ao quadrado e informar se o resultado é maior do que 100.

//entrada
var num = 25

//processamento
var calc = num * num;

//saída
if (calc > 100) {
	console.log(num, "ao quadrado é", calc, "que é maior que 100");
} else {
	console.log(num, "ao quadrado é", calc, "que não é maior que 100");
}
