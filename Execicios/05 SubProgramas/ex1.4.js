// Implementar uma função que recebe como parâmetro quatro
// números e retorna o valor da soma dos seus quadrados

function zard (a,b,c,d) {
	a = a * a
	// console.log(a)
	b = b * b
	// console.log(b)
	c = c * c
	// console.log(c)
	d = d * d
	// console.log(d)

	return a + b + c + d
}


console.log(zard(2,4,6,8))